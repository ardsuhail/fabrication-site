// Helper functions for filtering
export const getProjectById = (id) => projects.find(p => p.id === id);
export const getProjectsByCategory = (category) => projects.filter(p => p.category === category);
export const getRelatedProjects = (currentId, limit = 3) => {
  const current = projects.find(p => p.id === currentId);
  if (!current) return [];
  return projects
    .filter(p => p.id !== currentId && p.category === current.category)
    .slice(0, limit);
};
export const getAllCategories = () => [...new Set(projects.map(p => p.category))];
export const getAllLocations = () => [...new Set(projects.map(p => p.location))];